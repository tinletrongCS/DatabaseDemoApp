package org.example.model;

public class SanPhamBanChay {
    private String loaiSanPham;
    private String maSanPham;
    private String tenSanPham;
    private String tenShop;
    private int soDonHang;
    private int tongSoLuongDaBan;
    private double giaTrungBinh;
    private double tongDoanhThu;

    // Getters and Setters
    public String getLoaiSanPham() {
        return loaiSanPham;
    }

    public void setLoaiSanPham(String loaiSanPham) {
        this.loaiSanPham = loaiSanPham;
    }

    public String getMaSanPham() {
        return maSanPham;
    }

    public void setMaSanPham(String maSanPham) {
        this.maSanPham = maSanPham;
    }

    public String getTenSanPham() {
        return tenSanPham;
    }

    public void setTenSanPham(String tenSanPham) {
        this.tenSanPham = tenSanPham;
    }

    public String getTenShop() {
        return tenShop;
    }

    public void setTenShop(String tenShop) {
        this.tenShop = tenShop;
    }

    public int getSoDonHang() {
        return soDonHang;
    }

    public void setSoDonHang(int soDonHang) {
        this.soDonHang = soDonHang;
    }

    public int getTongSoLuongDaBan() {
        return tongSoLuongDaBan;
    }

    public void setTongSoLuongDaBan(int tongSoLuongDaBan) {
        this.tongSoLuongDaBan = tongSoLuongDaBan;
    }

    public double getGiaTrungBinh() {
        return giaTrungBinh;
    }

    public void setGiaTrungBinh(double giaTrungBinh) {
        this.giaTrungBinh = giaTrungBinh;
    }

    public double getTongDoanhThu() {
        return tongDoanhThu;
    }

    public void setTongDoanhThu(double tongDoanhThu) {
        this.tongDoanhThu = tongDoanhThu;
    }
}
